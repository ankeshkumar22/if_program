import java.util.*;

public class VowelConsonant {
    public static void main(String args []){
        char c = 'a';

        if (c== 'a' || c== 'e' || c== 'i' || c== 'o' || c=='u') {
            System.out.println(c + " is Vowel");
        }
        else {
            System.out.println(c + " is  consonant");
        }
    }
}